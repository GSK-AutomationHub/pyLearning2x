# Learning List methods continued ..

courses = ['Physics', 'History', 'CompSci', 'Geography', 'Math']
num = [1, 3, 6, 5, 9, 0]

# min, max and sum methods
print(min(num))
print(max(num))
print(sum(num))

# index to print index of list value
print(courses.index('CompSci'))

# in used to check if value exist in list, it return boolean value
print('art' in courses)
print('art' not in courses)