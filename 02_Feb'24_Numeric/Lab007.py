# Learning python arithmetic operators

# Type function to find data type of literal
Num = 3
print(type(Num))
Num = 3.14
print(type(Num))

# Arithmetic operators
Num_1 = 24
Num_2 = 6
print(Num_1 + Num_2)
print(Num_1 - Num_2)
print(Num_1 * Num_2)
print(Num_1 / Num_2)

# Arithmetic operators // (floor division) ,** (Exponent), % (Modulus)
Num_1 = 5
Num_2 = 3
print(Num_1 // Num_2)
print(Num_1 ** Num_2)
print(Num_1 % Num_2)

# Order of operation
print(Num_1 * Num_2 + 6)
print(Num_1 * (Num_2 + 6))