# Learning python strings Basics

# Here message is identifier and the 'hello world' msg is string literal
message = 'Hello world'
print(message)

# To avoid error we can use escape character \ to print string literal
message = 'Ganesh\'s World'
print(message)

# Can also put literal in double quote
message = "Ganesh's World"
print(message)

# An example of triple quote to print multiline string
message = '''this is multiline text message 
to show usage of triple quote'''
print(message)

# type method return the data tye of literal
print(type(message))
